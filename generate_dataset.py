# 提取所有站点
all_stations = []
for line_info in METRO_DATA.values():
    all_stations.extend(line_info["stations"])
all_stations = list(set(all_stations))

# 丰富的自然语言模板
templates = [
    "从{}到{}", "我要从{}去{}", "{}到{}怎么走", "目的地{}，起点{}",
    "从{}出发到{}", "坐地铁从{}到{}", "怎么去{}从{}", "{}前往{}",
    "我在{}去{}", "出发地{}到达{}"
]

# 生成500条高质量数据集（解决数据不足问题）
dataset = []
for _ in range(500):
    s1 = random.choice(all_stations)
    s2 = random.choice(all_stations)
    if s1 != s2:
        text = random.choice(templates).format(s1, s2)
        dataset.append([text, s1, s2])

# 保存独立CSV数据集
df = pd.DataFrame(dataset, columns=["query_text", "start_station", "end_station"])
df.to_csv("metro_nlp_dataset.csv", index=False, encoding="utf-8-sig")

